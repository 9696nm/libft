#include "../include/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL || f == NULL)
		return ;
	if (lst->content)
		(f)(lst->content);
	if (lst->next)
		ft_lstiter(lst->next, (*f));
	return ;
}
